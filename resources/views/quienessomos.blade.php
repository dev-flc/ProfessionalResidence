	<h2>Nuestra historia</h2>

	<p class="p">
La Escuela Normal Urbana Federal “Profr. Rafael Ramírez” fue fundada en la administración del Ingeniero Rubén Figueroa, quién fungía como Gobernador del Estado.
Los motivos de su fundación fueron justificados por el Coordinador de Educación en el Estado Profr. Raúl Pous Ortiz, y una de las causas fue la demanda de la juventud Guerrerense por ingresar a la Escuela Normal del Estado.
Así se conjugaron estas demandas y se creó la Normal Urbana Federal en turno vespertino, el viernes 13 de enero de 1976 y sus clases se iniciaron el 16 del mismo mes y año, nombrando al Profr. Felipe Carreto Arriaga Director y al Profr. Carlos Orraca Varela, como Subdirector.</p>
<p class="p">

La infraestructura ocupada por la naciente escuela, fue el edificio que el Gobernador Raymundo Abarca Alarcón, mandó construir para la Normal del Estado cita en la calle 18 de Marzo e Ignacio Ramírez. Por gestiones de las autoridades Educativas y del propio Gobernador Rubén Figueroa, en la parte poniente de la Ciudad de Chilpancingo se construyó el Centro Escolar “Vicente Guerrero” y en estos espacios se construyeron Escuelas de nivel Básico, Medio, Medio Superior y Superior; incluyendo la Normal Urbana Federal; Inaugurada por el Sr. Presidente de la República José López Portillo el 28 de febrero de 1981.
</p>
<p class="p">
Desde 1981 hasta 1994 se compartió la Escuela, el turno Matutino que es la Centenaria Escuela Normal del Estado “Ignacio Manuel Altamirano” y el Vespertino, Normal Urbana Federal. Desde su fundación, la institución educativa se le reconoció como Escuela Normal Urbana Federal de Chilpancingo.
En 1983 el alumnado sugiere a la Dirección de la Escuela que se le asigne un nombre. La subdirección académica, asesora al alumnado y convoca a un concurso para seleccionar un nombre, acorde con los objetivos que se persiguen en la carrera. Para dar fe de la elección del nombre, se reunieron los asesores de grupo, el jefe del Departamento de promotoría cultural, jefes de grupo y Directivos de la Normal.
</p>
<p class="p">
Por su amplia trayectoria en el normalismo y por haber sido el impulsor de la Escuela Primaria Rural Mexicana, es elegido el nombre del insigne maestro Veracruzano Rafael Ramírez Castañeda. En respuesta se autoriza a la Escuela Normal por oficio fechado el 7 de julio de 1983 que se ordene la fabricación de un sello con el escudo nacional y con el nombre Escuela Normal Urbana Federal “Profr. Rafael Ramírez”.
</p>
<p class="p">
Momento importante en la historia de la Normal ha sido la construcción del nuevo edificio que actualmente se ocupa y que fue gestionado por el Profr. Israel Rivera Serrano, por la profra. Felipa Cuenca Adame y el Profr. Javier Soberanis Méndez directivos de esa época. Este edificio fue inaugurado por el Gobernador Rubén Figueroa Alcocer, 15 de septiembre de 1994
	</p>
